class SBDatasetWrapper:
    """SBDatasetWrapper(dataset, flag_train, loader_kwargs)

    This is a wrapper over speechbrain dataset.
    It should be compatible with the training/inference scripts in nn_wrappper.

    Args
    ----
      dataset:        speechbrain dataset object
      flag_train:     bool, whether this is in training mode or not
      loader_kwargs:  kwargs used for speechbrain dataloader
                      for example, in hyaml
      f_save_item:    a function to save data in a specified format
                      if None, we will use the default method

    Here is one example on loader_kwargs:
    dataloader_eval_options:
        batch_size: 1
        shuffle: False
        num_workers: !ref <num_workers>
        collate_fn: !name:core_scripts.data_io.sb_collate.PaddedBatch_customize
    """
    def __init__(self, dataset, flag_train, loader_kwargs, f_save_item = None):
        
        # link dataset
        self.dataset = dataset

        # flag
        self.flag_train = flag_train

        # create data loader
        self.data_loader = make_dataloader(dataset, flag_train, **loader_kwargs)

        # name tagger
        self.db_type = 'speechbrain'

        # method to save data
        if f_save_item is None:
            self.f_save_item = f_sb_default_putitem
        else:
            self.f_save_item = f_save_item 
        return
        
    def print_info(self):
        """print_info(self)
        Print basic information about the dataset.
        Since no pre-check is done on the data, we don't know the statics.
        Just show the number of data samples here.
        """
        mes = "Dataset using speechbrain dataset IO"
        mes += "\n  Number of data samples: {:d}".format(self.get_seq_num())
        print(mes)
        return

    def get_loader(self):
        """dataloader = get_loader()
        return a data loader
        """
        return self.data_loader
    
    def get_seq_num(self):
        """seq_num = get_seq_num()
        return the number of data sample (sequences) in the dataset
        """
        return len(self.dataset.data)

    def putitem(self, data_gen, save_dir, name_prefix, seq_info):
        """putitem(data_gen, save_dir, name, seq_info)
        save data_gen into a specified format at a specified place
        
        This is to simulat the behavior of f_putitem in default_data_io.
        This will be called in nn_manager.py.f_inference_wrapper_sb to save
        the output generated by a model.
        """
        # read the sentence information
        tmp_seq_info = nii_seqinfo.SeqInfo()
        tmp_seq_info.parse_from_str(seq_info)

        # write the data
        file_name = tmp_seq_info.seq_tag()
        file_name = name_prefix + file_name 
        
        # create the parent directory if necessary
        file_path = Path(save_dir) / file_name
        file_path.parent.mkdir(parents=True, exist_ok=True)

        self.f_save_item(data_gen, file_path)
        return


