def f_sb_default_putitem(output_data, file_path):
    """f_sb_default_putitem(output_data, save_dir, filename_prefix, data_infor)
    
    This default putitem simply save output data as *.bin in the save_dir
    input
    -----
      output_data:      torch tensor, any shape
      file_path:        str or Path, path to save the file
    """
    # save using the default IO as binary data
    nii_io_tk.f_write_raw_mat(output_data.numpy(),file_path.with_suffix('.bin'))
    return


